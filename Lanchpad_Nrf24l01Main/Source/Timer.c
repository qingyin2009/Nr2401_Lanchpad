/*******************************************************************************\
名称：TimerBase_Init(void)
作用：时基定时器初始化
参数：无
描述：无
\*******************************************************************************/
void TimerBase_Init(void)
{
  TACCR0 = 1000 - 1;                                //1us(8/8M)  * 1000 = 1ms
  TA0CTL = TASSEL1 + ID0 + ID1 + MC0 + TAIE;    //SMCLK + 8分频 + up Mode +中断允许
  TACCTL0 = 0x00;                               //输入捕捉和输出比较关闭
}

/*******************************************************************************\
名称：IntTimerBase(void)
作用：时基定时器中断
参数：无
描述：called by 1ms
\*******************************************************************************/
void IntTimerBase(void)
{
  P1OUT |= 0x01;
  P1OUT &= ~0x01;
  
  gbTimer1msSet1;        //置1ms标志
}

